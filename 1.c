#include <stdio.h>  // Бібліотека для стандартного введення/виведення (printf, scanf)

int main() {
    int n, m;       // n - рядки, m - стовпці
    int choice;     // Змінна для вибору в меню

    do {
        // Виведення тексту меню
        printf("\n========== МЕНЮ МАТРИЦІ ==========\n");
        printf("1. Ввести матрицю та знайти (max+min)/2\n");
        printf("0. Вихід\n");
        printf("Ваш вибір: ");
        scanf("%d", &choice); // Зчитуємо вибір користувача

        if (choice == 1) {
            // Запитуємо розміри
            printf("Введіть кількість рядків (n): ");
            scanf("%d", &n);
            printf("Введіть кількість стовпців (m): ");
            scanf("%d", &m);

            int A[n][m]; // Створюємо двовимірний масив (матрицю)

            // --- ВВЕДЕННЯ ЕЛЕМЕНТІВ ---
            printf("Заповніть матрицю цілими числами:\n");
            for (int i = 0; i < n; i++) {       // Зовнішній цикл (іде по рядках)
                for (int j = 0; j < m; j++) {   // Внутрішній цикл (іде по стовпцях)
                    printf("Елемент [%d][%d]: ", i, j);
                    scanf("%d", &A[i][j]);      // Записуємо число в комірку матриці
                }
            }

            // --- ВИВІД МАТРИЦІ ---
            printf("\nВаша матриця:\n");
            for (int i = 0; i < n; i++) {       // Проходимо по рядках
                printf("| ");                   // Малюємо ліву межу рядка
                for (int j = 0; j < m; j++) {   // Проходимо по стовпцях рядка
                    // %4d - виділяє 4 позиції для числа (вирівнювання)
                    printf("%4d ", A[i][j]); 
                }
                printf(" |\n");                 // Малюємо праву межу та переходимо на новий рядок
            }

            // --- ПОШУК MAX ТА MIN ---
            int max = A[0][0]; // Припускаємо, що перший елемент - найбільший
            int min = A[0][0]; // Припускаємо, що перший елемент - найменший

            for (int i = 0; i < n; i++) {
                for (int j = 0; j < m; j++) {
                    if (A[i][j] > max) {        // Якщо знайшли число більше за max
                        max = A[i][j];          // Оновлюємо значення max
                    }
                    if (A[i][j] < min) {        // Якщо знайшли число менше за min
                        min = A[i][j];          // Оновлюємо значення min
                    }
                }
            }

            // --- ОБЧИСЛЕННЯ СЕРЕДНЬОГО ---
            // (float) перетворює цілі числа на дробові, щоб результат був точним
            float avg = (float)(max + min) / 2;

            // Виведення підсумку
            printf("\n----------------------------------");
            printf("\nМаксимум: %d, Мінімум: %d", max, min);
            printf("\nСереднє арифметичне (max+min)/2 = %.2f\n", avg);
            printf("----------------------------------\n");
        }

    } while (choice != 0); // Продовжуємо, поки користувач не введе 0

    printf("Програма завершена.\n");
    return 0; // Повертаємо 0 (ознака успіху)
}